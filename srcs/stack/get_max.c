#include "push_swap.h"

int	get_max(t_stack *stack)
{
	t_node	*tmp;
	int		max;

	tmp = stack->top;
	max = tmp->data;
	while (tmp->bellow)
	{
		tmp = tmp->bellow;
		if (max < tmp->data)
			max = tmp->data;
	}
	return (max);
}
